	/**
	 * Returns the properly formatted name for this Item.
	 * @return
	 */
	public String getName() {
		return NameFormatter.get(toString());
	}

	/**
	 * Returns the price of this item. See {@link ItemUtil#getPrice(int)}.
	 * @return
	 */
	public int getPrice() {
		return ItemUtil.getPrice(this.id[0]);
	}

	/**
	 * Returns if an RSItem matches a set of ItemNames.
	 * @param check
	 * @param types
	 * @return
	 */
	public static boolean is(RSItem check, ItemNames... types) {
		for (int i = 0; i < types.length; i++) {
			for (int j = 0; j < types[i].getIds().length; j++) {
				if (types[i].getIds()[j] == check.getID()) {
					return true;
				}
			}
		}
		return false;
	}

	/**
	 * Returns if an RSGrountItem matches a set of ItemNames.
	 * @param check
	 * @param types
	 * @return
	 */
	public static boolean is(RSGroundItem check, ItemNames... types) {
		for (int i = 0; i < types.length; i++) {
			for (int j = 0; j < types[i].getIds().length; j++) {
				if (types[i].getIds()[j] == check.getID()) {
					return true;
				}
			}
		}
		return false;
	}

	/**
	 * Returns if this ItemNames enum contains a specific item id.
	 * @param array
	 * @return
	 */
	public boolean hasId(int[] array) {
		int[] t = getIds();
		for (int i = 0; i < t.length; i++) {
			for (int a = 0; a < array.length; a++) {
				if (t[i] == array[a])
					return true;
			}
		}
		return false;
	}

	/**
	 * Combines the IDs of a list of Itemnames
	 * @param itemNames
	 * @return
	 */
	public static int[] get(ItemNames... itemNames) {
		int len = 0;
		for (int i = 0 ;i < itemNames.length; i++) {
			len += itemNames[i].getIds().length;
		}
		
		int a = 0;
		int[] ret = new int[len];
		for (int i = 0; i < itemNames.length; i++) {
			int[] t = itemNames[i].getIds();
			for (int j = 0; j < t.length; j++) {
				ret[a++] = t[j];
			}
		}
		
		return ret;
	}
